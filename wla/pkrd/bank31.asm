	LD E, (HL)
	LD HL, $C02E
	ADD HL, BC
	BIT 3, (HL)
	JR Z, Audio3_ApplyMusicAffects.unset
	RES 3, (HL)
	LD A, D
	AND $0F
	LD D, A
	LD A, E
	SUB D
	JR NC, Audio3_ApplyMusicAffects.noCarry
	LD A, $00
