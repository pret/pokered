; default hidden/shown objects for each map

MapHSPointers:
; entries correspond to map ids
	table_width 2
FOR n, NUM_MAPS
	IF DEF(HSMAP{n}) ; defined by `hs_map`
		dw HSData{n}
	ELSE
		dw NoHS
	ENDC
ENDR
	assert_table_length NUM_MAPS
	dw -1 ; end

NoHS:
	db -1, -1, -1 ; end

DEF hs_okay = 1

MACRO? hs_objects_for
	DEF hs_map_id = \1 ; map id
	HSData{hs_map_id}:
	IF hs_okay
		ASSERT DEF(HSMAP{hs_map_id}), "`hs_map \1` is not defined"
		DEF hs_okay &= DEF(HSMAP{hs_map_id})
		IF hs_okay
			assert_table_length HSMAP{hs_map_id}
			DEF hs_okay &= HSMAP{hs_map_id} * 3 == @ - MissableObjects
		ENDC
	ENDC
ENDM

MACRO hs_object
	db hs_map_id ; from previous `hs_objects_for`
	db \1 ; object id
	db \2 ; HIDE/SHOW
ENDM

